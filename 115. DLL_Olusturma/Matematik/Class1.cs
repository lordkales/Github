namespace Matematik
{
    public class MatematikIslemleri
    {
        public int topla(int sayi1,int sayi2)
        {
            return sayi1+ sayi2;
        }
        public int cıkartma(int sayi1,int sayi2) 
        {
            return sayi1 - sayi2;
        }
        public int carpma(int sayi1,int sayi2)
        {
            return sayi1 * sayi2;
        }
        public double bolme(double sayi1,double sayi2)
        {
            return sayi1 / sayi2;
        }
    }
}
